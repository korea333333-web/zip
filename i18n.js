let currentLang = localStorage.getItem('zipEasyLang') || 'ko';

function t(key) {
    return translations[currentLang]?.[key] || translations['ko'][key] || key;
}

function applyLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('zipEasyLang', lang);
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : lang;

    // Update all data-i18n elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const val = t(key);
        if (el.tagName === 'INPUT') {
            el.placeholder = val;
        } else {
            el.innerHTML = val;
        }
    });

    // Update lang selector display
    const langToggle = document.getElementById('langSelect');
    if (langToggle) langToggle.value = lang;
}

function initI18n() {
    const langSelect = document.getElementById('langSelect');
    if (langSelect) {
        langSelect.value = currentLang;
        langSelect.addEventListener('change', (e) => {
            applyLanguage(e.target.value);
        });
    }
    applyLanguage(currentLang);
}
